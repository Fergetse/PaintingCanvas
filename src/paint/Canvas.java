    @Override
    public void paintComponent(Graphics g) {
        super.paintComponent(g);
        chargeLines(g);
        drawLine(g);
    }

    public void drawLine(Graphics g) {
        g.setColor(color);
        g.fillOval(xTotal - (size / 2), yTotal - (size / 2), size, size);
        lista.add(new Draw(xTotal, yTotal, color, size));
    }

    public void chargeLines(Graphics g) {
        Draw dib;
        for (int i = 0; i < lista.size(); i++) {
            dib = lista.get(i);
            g.setColor(dib.getColor());
            g.fillOval(dib.getX() - (dib.getSize() / 2), dib.getY() - (dib.getSize() / 2), dib.getSize(), dib.getSize());
        }
    }

